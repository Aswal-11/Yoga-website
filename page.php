                    <li>Improves balance and core strength</li>
                    <li>Builds stamina and concentration</li>
                    <li>Stimulates abdominal organs</li>
                </ul>
                
                <h3 class="text-2xl font-semibold mb-4">How to Do Warrior II</h3>
                <ol class="list-decimal list-inside mb-4">
                    <li>Stand with your feet wide apart, about 3 to 4 feet.</li>
                    <li>Turn your right foot out 90 degrees and your left foot in slightly.</li>
                    <li>Raise your arms parallel to the floor, palms facing down.</li>
                    <li>Bend your right knee over your right ankle, keeping your shin perpendicular to the floor.</li>
                    <li>Turn your head to look over your right fingertips.</li>
                    <li>Hold the pose for 30 seconds to 1 minute, then repeat on the other side.</li>
                </ol>
            </div>
