                <h3 class="text-2xl font-semibold mb-4">About the Pose</h3>
                <p class="mb-4">Warrior II, or Virabhadrasana II, is a powerful standing pose that builds strength, stability, and concentration. This pose is named after the fierce warrior Virabhadra, an incarnation of the Hindu god Shiva.</p>
                
                <h3 class="text-2xl font-semibold mb-4">Benefits</h3>
                <ul class="list-disc list-inside mb-4">
                    <li>Strengthens legs, ankles, and feet</li>
