            <ul class="list-disc list-inside mb-4">
                <li>Keep your back heel grounded for stability.</li>
                <li>Engage your core to maintain balance.</li>
                <li>Don't lean your upper body over your front leg.</li>
                <li>If you feel strain in your knee, widen your stance.</li>
